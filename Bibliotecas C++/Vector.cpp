#include <iostream>
#include <vector> // Para trabalhar com vetores

/*
*   VECTOR
*   O std::vector é um contêiner sequencial que armazena elementos de forma contígua,
*   permitindo o acesso eficiente aos elementos por índice.
*   vector<Type> variable;
*   Funções principais do std::vector:
*   push_back(value): Adiciona um elemento no final do vetor.
*   pop_back(): Remove o último elemento do vetor.
*   at(index): Acessa o elemento no índice especificado (verificação de limites).
*   operator[]: Acessa o elemento no índice especificado (sem verificação de limites).
*   size(): Retorna o número de elementos no vetor.
*   empty(): Retorna true se o vetor estiver vazio, caso contrário, retorna false.
*   clear(): Remove todos os elementos do vetor.
*   front(): Retorna o primeiro elemento do vetor.
*   back(): Retorna o último elemento do vetor.
*   insert(position, value): Insere um elemento na posição especificada.
*   erase(position): Remove o elemento na posição especificada.
*/