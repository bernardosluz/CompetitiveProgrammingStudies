/*
*   UNORDERED_MAP
*   O std::unordered_map é uma estrutura de dados que armazena pares chave-valor
*   de forma não ordenada, permitindo acesso rápido aos elementos por chave.
*   unordered_map<KeyType, ValueType> variable;
*   Funções principais do std::unordered_map:
*   insert(pair<KeyType, ValueType>): Insere um par chave-valor no mapa.
*   erase(key): Remove o elemento com a chave especificada.
*   find(key): Retorna um iterador para o elemento com a chave fornecida (se existir).
*   operator[]: Acessa o valor associado à chave. Se a chave não existir, cria um novo valor.
*   size(): Retorna o número de elementos no mapa.
*   empty(): Retorna true se o mapa estiver vazio, caso contrário, retorna false.
*   clear(): Remove todos os elementos do mapa.
*/

