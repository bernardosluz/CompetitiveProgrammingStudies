#include <iostream>
#include <stack> // Para trabalhar com pilhas

/*
*   STACK
*   O std::stack é uma estrutura de dados do tipo LIFO (Last In, First Out), 
*   onde só é possível acessar o topo da pilha.
*   stack<Type> variable;
*   Funções principais do std::stack:
*   push(value): Adiciona um elemento no topo da pilha.
*   pop(): Remove o elemento do topo da pilha.
*   top(): Retorna o elemento do topo da pilha.
*   empty(): Retorna true se a pilha estiver vazia, caso contrário, retorna false.
*   size(): Retorna o número de elementos na pilha.
*/