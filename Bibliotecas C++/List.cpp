#include <iostream>
#include <list> // Para trabalhar com listas

/*
*   LIST
*   O std::list é uma estrutura de dados de lista duplamente encadeada que permite
*   inserção e remoção eficientes em ambas as extremidades e no meio da lista.
*   list<Type> variable;
*   Funções principais do std::list:
*   push_back(value): Adiciona um elemento no final da lista.
*   push_front(value): Adiciona um elemento no início da lista.
*   pop_back(): Remove o elemento do final da lista.
*   pop_front(): Remove o elemento do início da lista.
*   insert(position, value): Insere um elemento na posição especificada.
*   erase(position): Remove o elemento na posição especificada.
*   size(): Retorna o número de elementos na lista.
*   empty(): Retorna true se a lista estiver vazia, caso contrário, retorna false.
*   clear(): Remove todos os elementos da lista.
*   front(): Retorna o primeiro elemento da lista.
*   back(): Retorna o último elemento da lista.
*/